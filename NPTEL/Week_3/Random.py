import random
print("Randint function: ",random.randint(1,3))
# it'll print 1 2 3

print("Randrange function: ",random.randrange(1,3))
# it'll print 1 and 2

# randrange= lowerlimit to (upperllimit-1)

print("More on Randrange function: ",random.randrange(3,10,3))

print("Random function: ",random.random())
# it'll print decimal between 0 to 1

